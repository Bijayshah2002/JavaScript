const month=6
switch (month) {
    case 1:
        console.log("January")
        break;
    case 2:
        console.log("Feb")
        break;
    case 3:
        console.log("march")
        break;
    case 4:
        console.log("april")
        break;
    case 5:
        console.log("May")
        break;
    case 6:
        console.log("june")
        break;

    default:
        console.log("not a month")
        break;
}